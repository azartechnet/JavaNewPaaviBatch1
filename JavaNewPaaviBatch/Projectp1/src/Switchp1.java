
public class Switchp1 {

	public static void main(String[] args) {
		
		//int exp1=6;
		String exp1="ones";
		
		switch(exp1)
		{
		case "ones":
			System.out.println("Case-1");
			break;
		case "twos":
			System.out.println("Case-2");
			break;
		case "threes":
			System.out.println("Case-3");
			break;
		default:
			System.out.println("default case");
			break;
		}
	}

}
